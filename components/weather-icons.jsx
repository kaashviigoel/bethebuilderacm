export function getWeatherIcon(condition, className) {
  const iconMap = {
    clear: <SunIcon className={className} />,
    sunny: <SunIcon className={className} />,
    cloudy: <CloudIcon className={className} />,
    "partly cloudy": <CloudSunIcon className={className} />,
    rain: <CloudRainIcon className={className} />,
    rainy: <CloudRainIcon className={className} />,
    snow: <SnowflakeIcon className={className} />,
    snowy: <SnowflakeIcon className={className} />,
    windy: <WindIcon className={className} />,
  }
  
  const lowerCondition = condition.toLowerCase()
  for (const [key, icon] of Object.entries(iconMap)) {
    if (lowerCondition.includes(key)) {
      return icon
    }
  }
  return <CloudSunIcon className={className} />
}
